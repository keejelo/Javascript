//----------------------------------------------------------------------------------------------------
// GetElement - combined shorthand for .querySelectorAll()
// Version: 0.1
// Created by: keejelo
// Year: 2010
//----------------------------------------------------------------------------------------------------
// Syntax: getEl(selectors, parentNode);  // <-- if parentNode not used then default is: document
//----------------------------------------------------------------------------------------------------
var getEl = function(s, pNode)
{
    return (pNode||document).querySelectorAll(s);
};
//----------------------------------------------------------------------------------------------------
// END: GetElement
